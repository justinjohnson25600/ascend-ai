                <h2 id="legal-basis">4. Legal Basis for Processing (UK GDPR)</h2>
                <p>Under UK GDPR, we must have a lawful basis for processing your personal data. We rely on the following legal bases:</p>

                <h4>4.1 Legitimate Interests (Article 6(1)(f) UK GDPR)</h4>
                <p>Our primary legal basis is <strong>legitimate interests</strong>. We process your personal data for:</p>
                <ul>
                    <li>Responding to business enquiries in a timely and efficient manner</li>
                    <li>Evaluating potential investment, partnership, or advisory opportunities</li>
                    <li>Maintaining accurate business records</li>
                    <li>Preventing fraudulent or malicious communications</li>
                </ul>
                <p>Our legitimate interests do not override your fundamental rights and freedoms. We process only the minimum data necessary to achieve these purposes.</p>

                <h4>4.2 Legal Obligation (Article 6(1)(c) UK GDPR)</h4>
                <p>We may process your data to comply with legal obligations, such as:</p>
                <ul>
                    <li>Tax and accounting record-keeping requirements</li>
                    <li>Anti-money laundering regulations</li>
                    <li>Court orders or legal requests from regulatory authorities</li>
                </ul>

                <h4>4.3 Consent (Article 6(1)(a) UK GDPR)</h4>
                <p>Where we send you information beyond what you initially requested, we will obtain your explicit consent first. You may withdraw your consent at any time by contacting us.</p>

