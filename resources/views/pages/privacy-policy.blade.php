                <h2 id="your-rights">7. Your Rights</h2>
                <p>Under UK GDPR, you have the following rights regarding your personal data:</p>

                <h4>7.1 Right of Access (Article 15)</h4>
                <p>You have the right to request a copy of the personal data we hold about you. We will provide this within 30 days of your request.</p>

                <h4>7.2 Right to Rectification (Article 16)</h4>
                <p>You have the right to request correction of inaccurate or incomplete personal data.</p>

                <h4>7.3 Right to Erasure (Article 17)</h4>
                <p>You have the right to request deletion of your personal data, subject to certain exceptions such as legal obligations.</p>

                <h4>7.4 Right to Restrict Processing (Article 18)</h4>
                <p>You have the right to request that we limit how we process your personal data in certain circumstances.</p>

                <h4>7.5 Right to Data Portability (Article 20)</h4>
                <p>You have the right to receive your personal data in a structured, commonly used format and to request that we transfer it to another organisation where technically feasible.</p>

                <h4>7.6 Right to Object (Article 21)</h4>
                <p>You have the right to object to processing based on legitimate interests. We will stop processing your data unless we have compelling legitimate grounds that override your interests.</p>

                <h4>7.7 Rights Related to Automated Decision-Making (Article 22)</h4>
                <p>We do not use automated decision-making or profiling, so this right is not applicable to our processing activities.</p>

                <h3>How to Exercise Your Rights</h3>
                <p>To exercise any of these rights, contact us at:</p>
